    private void ButtonEnter1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEnter1ActionPerformed
        //GUARDAR TODOS OS ESTADOS EM UM VETOR DE STRING
        //POLIMORFISMO NA FUNÇÃO DE ATUALIZAR A SCREE
        switch(State){
            case 0:
                if(Controller.verifyAccountNumber() == true){
                    State++;
                    GUI.accountNumber = entry;
                    GUI.entry = "";
                    AtualizeConsoleText();
                    GUI.ConsoleActualText = GUI.ConsoleText[1];
                    AtualizeConsole();
                }else{
                    JOptionPane.showMessageDialog(this, "Incorrect account number!");
                }
                break;

            case 1:
                if(Controller.verifyAccountPin(entry) == true){
                   GUI.State = 2;
                   GUI.accountPin  = entry;
                   GUI.entry = "";
                   Controller.Transiction.accountNumber = GUI.accountNumber;
                   GUI.ConsoleActualText = GUI.ConsoleText[2];
                   AtualizeConsole();
                } else {
                    JOptionPane.showMessageDialog(this, "Incorrect Pin Code for current account number!");
                    GUI.State = 0;
                    GUI.entry = "";
                    GUI.accountNumber = "";
                    GUI.ConsoleActualText = GUI.ConsoleText[0];
                    AtualizeConsole();
                }
            case 2:
                switch(entry){
                    case "1":
                        GUI.entry = "";
                        GUI.State = 10;
                        GUI.ConsoleActualText = GUI.ConsoleText[3];
                        GUI.nextState = 2;
                        AtualizeConsole();
                        break;

                    case "2":
                        GUI.entry = "";
                        GUI.State = 4;
                        GUI.ConsoleActualText = GUI.ConsoleText[4];
                        AtualizeConsole();
                        break;
                    case "3":
                        GUI.entry = "00000,00";
                        GUI.State = 3;
                        GUI.ConsoleActualText = GUI.ConsoleText[6];
                        AtualizeConsole();
                        break;
                        
                    case "4":
                        GUI.nextState = 0;
                        GUI.entry = "";
                        GUI.State = 10;
                        GUI.ConsoleActualText = "Thanks for use ATM!\n\n\n\nPress GO to exit.";
                        AtualizeConsole();
                        break;
                }
                break;
            case 3:
                String replaceFirst = GUI.entry.replaceFirst(",", ".");
                GUI.entry = replaceFirst;
                depositAmount = Float.parseFloat(GUI.entry);
                GUI.State = 6;
                GUI.ConsoleActualText = GUI.ConsoleText[7];
                GUI.entry = "";
                ButtonDeposit.setEnabled(true);
                AtualizeConsole();
                start = System.currentTimeMillis();
                break;

            case 4:
                float AmountTmp = 0;
                switch(entry){
                    case "1": AmountTmp = 20; break;
                    case "2": AmountTmp = 40; break;
                    case "3": AmountTmp = 60; break;
                    case "4": AmountTmp = 100; break;
                    case "5": AmountTmp = 200; break;

                    default:
                        GUI.entry = "";
                        GUI.State = 2;
                        GUI.ConsoleActualText = GUI.ConsoleText[2];
                        AtualizeConsole();
                        break;
                }

                if(GUI.State != 2){
                    Controller.WithDraw.setAmount(AmountTmp);

                    if(Controller.WithDraw.execute() == true){
                        GUI.entry = "";
                        GUI.State = 5;
                        GUI.ConsoleActualText = GUI.ConsoleText[5];
                        ButtonTake.setEnabled(true);
                        AtualizeConsole();
                    } else {
                        GUI.nextState = 4;
                        GUI.entry = "";
                        GUI.State = 10;
                        GUI.ConsoleActualText = "Limit exceeded!\n\n\n\n\n\nPress Go to back.";
                        AtualizeConsole();
                    }
                }
                break;
            
            case 5:
                break;
            case 6:
                break;
                
            case 10:
                GUI.State = GUI.nextState;
                GUI.ConsoleActualText = GUI.ConsoleText[GUI.nextState];
                AtualizeConsole();
                break;
        }
    }//GEN-LAST:event_ButtonEnter1ActionPerformed

