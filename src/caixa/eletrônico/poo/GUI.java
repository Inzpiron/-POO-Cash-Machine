    public void AtualizeConsole(String entry){
        AtualizeConsoleText();
        String tmp = GUI.ConsoleActualText;
        
        if (GUI.State == 3){
            if(GUI.entry.length() <= 8){
                String replaceFirst = GUI.entry.replaceFirst(",", ".");
                GUI.entry = replaceFirst;
                float addFloat = Float.parseFloat(entry)/100f;
                float actual = Float.parseFloat(GUI.entry)*10f + addFloat;
                DecimalFormat df = new DecimalFormat("00000.00");
                GUI.entry = df.format(actual);
            }
        }else
            GUI.entry += entry;
        
        if(GUI.State != 10 && GUI.State != 5)
            tmp += GUI.entry;
        Console.setText(tmp);
    }
    
    public void AtualizeConsole(){
        AtualizeConsoleText();
        String tmp = GUI.ConsoleActualText;
        if(GUI.State != 10 && GUI.State != 5)
            tmp += entry;
        Console.setText(tmp);
    }
